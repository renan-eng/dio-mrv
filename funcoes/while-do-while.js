function exemploWhile() {
    let num = 0;

    while(num < 10) {
        console.log(num);
        num++;
    }
}

exemploWhile()

// do-while executa o código pelo menos uma vez antes de checar a condição
function exemploDoWhile() {
    let num = 11;

    do{
        console.log(num);
        num++;
    }while(num < 10)
}

exemploDoWhile()